# Stages of Life
person_age = 80

if 0 < person_age < 2:
    print("You are still a baby.")
elif person_age < 4:
    print("You are a toddler.")
elif person_age < 13:
    print("You are a kid.")
elif person_age < 20:
    print("You are a teenger now")
elif person_age < 65:
    print("You are now an adult")
elif person_age >= 65:
    print("You are officially an elder!")